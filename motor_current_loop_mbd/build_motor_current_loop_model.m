function p = default_current_loop_params()
p.simcfg.stop_time = 0.050;
p.simcfg.Ts_plant = 25e-6;
p.simcfg.Ts_ctrl = 50e-6;

p.inverter.Vdc = 68;
p.inverter.load_torque = 0;

p.motor.type = 'SPMSM';
p.motor.pole_pairs = 10;
p.motor.back_emf_vrms_per_krpm = 17.03;
p.motor.line_to_line_resistance = 0.4267;
p.motor.line_to_line_inductance = 0.53e-3;
p.motor.Rs = p.motor.line_to_line_resistance / 2;
p.motor.Ld = p.motor.line_to_line_inductance / 2;
p.motor.Lq = p.motor.Ld;
p.motor.psi_f = p.motor.back_emf_vrms_per_krpm / ...
    (sqrt(3/2) * p.motor.pole_pairs * (1000 * 2 * pi / 60));
p.motor.J = 2.5e-4;
p.motor.B = 1.0e-4;

current_bandwidth_rad_s = 2 * pi * 800;
p.current_loop.Kp_id = single(p.motor.Ld * current_bandwidth_rad_s);
p.current_loop.Ki_id = single(p.motor.Rs * current_bandwidth_rad_s);
p.current_loop.Kaw_id = single(400);
p.current_loop.Kp_iq = p.current_loop.Kp_id;
p.current_loop.Ki_iq = p.current_loop.Ki_id;
p.current_loop.Kaw_iq = p.current_loop.Kaw_id;
p.current_loop.VLimitRatio = single(0.577);

p.current_loop.ref.id_step_time = 0.005;
p.current_loop.ref.id_before = single(0);
p.current_loop.ref.id_after = single(3);
p.current_loop.ref.iq_ref = single(0);
end

function ensure_source_models(repo_dir)
source_specs = { ...
    fullfile(repo_dir, 'motor_current_pi_mbd', 'current_pi_model.slx'), ...
    fullfile(repo_dir, 'motor_current_pi_mbd', 'build_current_pi_model.m'); ...
    fullfile(repo_dir, 'motor_float_open_loop_mbd', 'motor_float_open_loop_model.slx'), ...
    fullfile(repo_dir, 'motor_float_open_loop_mbd', 'build_motor_float_open_loop_model.m'); ...
    fullfile(repo_dir, 'motor_clarke_park_struct', 'motor_clarke_park_model.slx'), ...
    fullfile(repo_dir, 'motor_clarke_park_struct', 'build_motor_clarke_park_model.m')};

for i = 1:size(source_specs, 1)
    if ~exist(source_specs{i, 1}, 'file')
        run(source_specs{i, 2});
    end
end
end

function source_models = load_source_models(repo_dir)
source_models.currentPi = 'current_pi_model';
source_models.openLoop = 'motor_float_open_loop_model';
source_models.clarkePark = 'motor_clarke_park_model';

load_model_from_folder(fullfile(repo_dir, 'motor_current_pi_mbd'), ...
    source_models.currentPi);
load_model_from_folder(fullfile(repo_dir, 'motor_float_open_loop_mbd'), ...
    source_models.openLoop);
load_model_from_folder(fullfile(repo_dir, 'motor_clarke_park_struct'), ...
    source_models.clarkePark);
end

function load_model_from_folder(model_dir, model_name)
previous_dir = pwd;
cleanup_dir = onCleanup(@() cd(previous_dir));
cd(model_dir);
load_system([model_name '.slx']);
end

function close_source_models(source_models)
names = {source_models.currentPi, source_models.openLoop, ...
    source_models.clarkePark};
for i = 1:numel(names)
    if bdIsLoaded(names{i})
        close_system(names{i}, 0);
    end
end
end

function close_open_data_dictionaries()
try
    Simulink.data.dictionary.closeAll('-discard');
catch err
    warning('Could not close open data dictionaries before rebuild: %s', ...
        err.message);
end
end


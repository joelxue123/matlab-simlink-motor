%% Build a float-based current-loop motor integration model
%
% Milestone:
%   id_ref step, iq_ref = 0
%
% This top-level model integrates reusable algorithm subsystems with the
% Average-Value Inverter and PMSM plant. The plant remains a simulation
% harness; controller subsystems remain the embedded-code candidates.

clc;

cfg = customer_interface_config();
script_dir = fileparts(mfilename('fullpath'));
repo_dir = fileparts(script_dir);
cfg.dictionaryFile = fullfile(script_dir, cfg.dictionaryName);
assignin('base', 'motor_current_loop_config', cfg);

previous_dir = pwd;
cleanup_dir = onCleanup(@() cd(previous_dir));
cd(script_dir);

ensure_source_models(repo_dir);

params = default_current_loop_params();
assignin('base', 'motor', params.motor);
assignin('base', 'inverter', params.inverter);
assignin('base', 'simcfg', params.simcfg);
assignin('base', 'current_loop', params.current_loop);
assignin('base', 'current_pi_simcfg', params.simcfg);

model = cfg.modelName;
model_file = fullfile(script_dir, [model '.slx']);

if bdIsLoaded(model)
    close_system(model, 0);
end

close_open_data_dictionaries();
create_current_loop_data_dictionary(cfg, params);

if exist(model_file, 'file')
    delete(model_file);
end

load_system('mcbplantlib');
load_system('mcblib');

source_models = load_source_models(repo_dir);

new_system(model);
set_param(model, 'DataDictionary', cfg.dictionaryName);
set_param(model, ...
    'StopTime', 'simcfg.stop_time', ...
    'SolverType', 'Fixed-step', ...
    'Solver', 'FixedStepDiscrete', ...
    'FixedStep', 'simcfg.Ts_plant', ...
    'SystemTargetFile', 'ert.tlc', ...
    'ProdHWDeviceType', 'ARM Compatible->ARM Cortex', ...
    'TargetHWDeviceType', 'ARM Compatible->ARM Cortex', ...
    'GenerateReport', 'on', ...
    'RTWVerbose', 'on', ...
    'GenCodeOnly', 'on', ...
    'GenerateSampleERTMain', 'off', ...
    'SupportContinuousTime', 'off', ...
    'InlineParams', 'off');

set_param(model, ...
    'CodeInterfacePackaging', 'Reusable function', ...
    'ModelReferenceNumInstancesAllowed', 'Multi');

copy_reusable_subsystems(model, source_models);
close_source_models(source_models);

add_current_reference_sources(model, cfg);
add_average_inverter_and_plant(model, cfg);
add_feedback_to_dq_adapter(model, cfg);
add_pi_input_adapter(model, cfg);
add_dq_voltage_to_duty_cmd_adapter(model, cfg);
connect_current_loop(model);
add_log_outputs(model);

set_param(model, 'SimulationCommand', 'update');
save_system(model, model_file);

fprintf('Built current-loop motor integration model:\n  %s\n', model_file);


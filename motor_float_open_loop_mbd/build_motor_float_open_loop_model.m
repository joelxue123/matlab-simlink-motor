%% Build a float-based open-loop motor simulation architecture
%
% Architecture:
%   open_loop_cmd_t -> DqToAbcDutyStep -> phase_duty_t
%   phase_duty_t + dc_bus_t -> Average-Value Inverter -> PMSM plant
%
% This is the first MBD architecture milestone for a new motor simulation
% stack. It intentionally uses single-precision float types everywhere at the
% controller interface. Fixed-point design can be introduced later through the
% same data-dictionary pattern.

clc;

cfg = customer_interface_config();
script_dir = fileparts(mfilename('fullpath'));
cfg.dictionaryFile = fullfile(script_dir, cfg.dictionaryName);
assignin('base', 'motor_float_open_loop_config', cfg);
previous_dir = pwd;
cleanup_dir = onCleanup(@() cd(previous_dir));
cd(script_dir);

open_loop_params = default_open_loop_params(cfg);
assignin('base', 'motor', open_loop_params.motor);
assignin('base', 'inverter', open_loop_params.inverter);
assignin('base', 'simcfg', open_loop_params.simcfg);
assignin('base', 'openloop', open_loop_params.openloop);

model = cfg.modelName;
model_file = fullfile(script_dir, [model '.slx']);

if bdIsLoaded(model)
    close_system(model, 0);
end

close_open_data_dictionaries();
create_motor_data_dictionary(cfg);

if exist(model_file, 'file')
    delete(model_file);
end

load_system('mcbplantlib');
load_system('mcblib');

new_system(model);
set_param(model, 'DataDictionary', cfg.dictionaryName);
set_param(model, ...
    'StopTime', 'simcfg.stop_time', ...
    'SolverType', 'Fixed-step', ...
    'Solver', 'FixedStepDiscrete', ...
    'FixedStep', 'simcfg.Ts_plant', ...
    'SystemTargetFile', 'ert.tlc', ...
    'ProdHWDeviceType', 'ARM Compatible->ARM Cortex', ...
    'TargetHWDeviceType', 'ARM Compatible->ARM Cortex', ...
    'GenerateReport', 'on', ...
    'RTWVerbose', 'on', ...
    'GenCodeOnly', 'on', ...
    'GenerateSampleERTMain', 'off', ...
    'SupportContinuousTime', 'off', ...
    'InlineParams', 'on');

set_param(model, ...
    'CodeInterfacePackaging', 'Reusable function', ...
    'ModelReferenceNumInstancesAllowed', 'Multi');

add_open_loop_command_subsystem(model, cfg);
add_dq_to_abc_duty_subsystem(model, cfg);
add_average_inverter_and_plant(model, cfg);
add_log_outputs(model, 'feedback_bus_creator/1');
add_duty_log_outputs(model);

set_param(model, 'SimulationCommand', 'update');
save_system(model, model_file);

fprintf('Built float open-loop motor simulation model:\n  %s\n', model_file);

function cfg = customer_interface_config()
% Customer-editable interface and architecture contract.
cfg.modelName = 'motor_float_open_loop_model';
cfg.sampleTime = 'simcfg.Ts_ctrl';
cfg.plantSampleTime = 'simcfg.Ts_plant';
cfg.rebuildDictionary = true;

cfg.dictionaryName = 'motor_float_interface.sldd';
cfg.typeHeaderFile = 'motor_float_types.h';

cfg.realTypeName = 'T_MotorFloat';
cfg.voltageTypeName = 'T_MotorVoltage';
cfg.angleTypeName = 'T_MotorAngle';
cfg.currentTypeName = 'T_MotorCurrent';
cfg.speedTypeName = 'T_MotorSpeed';
cfg.torqueTypeName = 'T_MotorTorque';
cfg.dutyTypeName = 'T_DutyRatio';

cfg.openLoopCmdBusName = 'open_loop_cmd_t';
cfg.phaseDutyBusName = 'phase_duty_t';
cfg.dcBusName = 'dc_bus_t';
cfg.plantFeedbackBusName = 'plant_feedback_t';


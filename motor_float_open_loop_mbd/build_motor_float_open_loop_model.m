end

function p = default_open_loop_params(~)
% Float-first baseline inspired by average-inverter/motor_control_params.m.
p.simcfg.stop_time = 0.20;
% Keep scheduler/sample-time values as double. Controller signal types are
% single, but Simulink block SampleTime parameters expect double scalars.
p.simcfg.Ts_plant = 25e-6;
p.simcfg.Ts_ctrl = 50e-6;

% Plant/library parameters stay double for robust Motor Control Blockset
% propagation. Controller-facing constants explicitly output T_MotorFloat.
p.inverter.Vdc = 68;
p.inverter.load_torque = 0;

p.motor.type = 'SPMSM';
p.motor.pole_pairs = 10;
p.motor.back_emf_vrms_per_krpm = 17.03;
p.motor.line_to_line_resistance = 0.4267;
p.motor.line_to_line_inductance = 0.53e-3;
p.motor.Rs = p.motor.line_to_line_resistance / 2;
p.motor.Ld = p.motor.line_to_line_inductance / 2;
p.motor.Lq = p.motor.Ld;
p.motor.psi_f = p.motor.back_emf_vrms_per_krpm / ...
    (sqrt(3/2) * p.motor.pole_pairs * (1000 * 2 * pi / 60));
p.motor.J = 2.5e-4;
p.motor.B = 1.0e-4;

p.openloop.vd = single(0);
p.openloop.vq = single(5);
p.openloop.freq_hz = single(20);
end

function close_open_data_dictionaries()
try
    Simulink.data.dictionary.closeAll('-discard');
catch err
    warning('Could not close open data dictionaries before rebuild: %s', ...
        err.message);
end
end

function create_motor_data_dictionary(cfg)
dd = open_or_create_data_dictionary(cfg);
design_data = getSection(dd, 'Design Data');

upsert_alias_type(design_data, cfg.realTypeName, cfg.floatBaseType, cfg.typeHeaderFile);
upsert_alias_type(design_data, cfg.voltageTypeName, cfg.floatBaseType, cfg.typeHeaderFile);
upsert_alias_type(design_data, cfg.angleTypeName, cfg.floatBaseType, cfg.typeHeaderFile);
upsert_alias_type(design_data, cfg.currentTypeName, cfg.floatBaseType, cfg.typeHeaderFile);
upsert_alias_type(design_data, cfg.speedTypeName, cfg.floatBaseType, cfg.typeHeaderFile);
upsert_alias_type(design_data, cfg.torqueTypeName, cfg.floatBaseType, cfg.typeHeaderFile);
upsert_alias_type(design_data, cfg.dutyTypeName, cfg.floatBaseType, cfg.typeHeaderFile);

upsert_bus_type(design_data, cfg.openLoopCmdBusName, cfg.typeHeaderFile, { ...
    {'vd', cfg.voltageTypeName}; ...
    {'vq', cfg.voltageTypeName}; ...
    {'theta_e', cfg.angleTypeName}; ...
    {'vdc', cfg.voltageTypeName}});

upsert_bus_type(design_data, cfg.phaseDutyBusName, cfg.typeHeaderFile, { ...
    {'da', cfg.dutyTypeName}; ...
    {'db', cfg.dutyTypeName}; ...
    {'dc', cfg.dutyTypeName}});

upsert_bus_type(design_data, cfg.dcBusName, cfg.typeHeaderFile, { ...
    {'vdc', cfg.voltageTypeName}});

upsert_bus_type(design_data, cfg.plantFeedbackBusName, cfg.typeHeaderFile, { ...
    {'ia', cfg.currentTypeName}; ...
    {'ib', cfg.currentTypeName}; ...
    {'ic', cfg.currentTypeName}; ...
    {'wm', cfg.speedTypeName}; ...
    {'theta_m', cfg.angleTypeName}; ...
    {'theta_e', cfg.angleTypeName}});

saveChanges(dd);
close(dd);
end

function dd = open_or_create_data_dictionary(cfg)
if isfield(cfg, 'rebuildDictionary') && cfg.rebuildDictionary && exist(cfg.dictionaryFile, 'file')
    delete(cfg.dictionaryFile);
end

if exist(cfg.dictionaryFile, 'file')
    try
        dd = Simulink.data.dictionary.open(cfg.dictionaryFile);
        getSection(dd, 'Design Data');
        return;
    catch err
        warning('Rebuilding data dictionary "%s" because it could not be opened: %s', ...
            cfg.dictionaryFile, err.message);
        close_open_data_dictionaries();
        backup_file = backup_bad_dictionary(cfg.dictionaryFile);
        fprintf('Backed up unusable data dictionary:\n  %s\n', backup_file);
    end
end

dd = Simulink.data.dictionary.create(cfg.dictionaryFile);
end

function backup_file = backup_bad_dictionary(dictionary_file)
[folder, name, ext] = fileparts(dictionary_file);
timestamp = datestr(now, 'yyyymmdd_HHMMSS');
backup_file = fullfile(folder, [name '_bad_' timestamp ext]);
index = 1;

while exist(backup_file, 'file')
    backup_file = fullfile(folder, sprintf('%s_bad_%s_%d%s', ...
        name, timestamp, index, ext));
    index = index + 1;
end

movefile(dictionary_file, backup_file);
end

function upsert_alias_type(section, type_name, base_type, header_file)
alias_type = Simulink.AliasType(base_type);
alias_type.DataScope = 'Exported';
alias_type.HeaderFile = header_file;

entry = find(section, 'Name', type_name);
if isempty(entry)
    addEntry(section, type_name, alias_type);
else
    setValue(entry(1), alias_type);
end
end

function upsert_bus_type(section, bus_name, header_file, element_specs)
bus_elements = repmat(Simulink.BusElement, numel(element_specs), 1);
for i = 1:numel(element_specs)
    bus_elements(i).Name = element_specs{i}{1};
    bus_elements(i).DataType = element_specs{i}{2};
    bus_elements(i).Dimensions = 1;
end

bus_type = Simulink.Bus;
bus_type.Elements = bus_elements;
bus_type.DataScope = 'Exported';
bus_type.HeaderFile = header_file;

entry = find(section, 'Name', bus_name);
if isempty(entry)
    addEntry(section, bus_name, bus_type);
else
    setValue(entry(1), bus_type);
end
end


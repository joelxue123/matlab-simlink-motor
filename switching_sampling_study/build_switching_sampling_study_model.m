    'Position', [origin(1) origin(2)-5 origin(1)+105 origin(2)+35]);
local_set_pwm_compare_script(compareBlk);
add_line(mdl, carrierSrc, ['Compare_' phaseName '/1']);
add_line(mdl, dutySrc, ['Compare_' phaseName '/2']);


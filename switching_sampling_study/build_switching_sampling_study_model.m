function local_set_duty_generator_script(blockPath)
rt = sfroot;
chart = rt.find('-isa', 'Stateflow.EMChart', 'Path', blockPath);
chart.Script = sprintf([ ...
    'function [da, db, dc] = fcn(theta_e_deg, modulation_ratio, Vdc, duty_sample_limit)\n' ...
    '%% Symmetric V0/V7 base duty plus MCU-style common duty shift\n' ...
    'theta_e = theta_e_deg * pi / 180;\n' ...
    'v_mag = modulation_ratio * (Vdc / sqrt(3));\n' ...
    'v_alpha = -v_mag * sin(theta_e);\n' ...
    'v_beta =  v_mag * cos(theta_e);\n' ...
    'va = v_alpha;\n' ...
    'vb = -0.5 * v_alpha + sqrt(3) / 2 * v_beta;\n' ...
    'vc = -0.5 * v_alpha - sqrt(3) / 2 * v_beta;\n' ...
    'duty_raw = [va; vb; vc] / Vdc + 0.5;\n' ...
    'shift_min = -min(duty_raw);\n' ...
    'shift_max = 1.0 - max(duty_raw);\n' ...
    'shift = 0.5 * (shift_min + shift_max);\n' ...
    'duty = min(max(duty_raw + shift, 0.0), 1.0);\n' ...
    'if (va >= vb) && (vb >= vc)\n' ...
    '    sector = 1;\n' ...
    'elseif (vb >= va) && (va >= vc)\n' ...
    '    sector = 2;\n' ...
    'elseif (vb >= vc) && (vc >= va)\n' ...
    '    sector = 3;\n' ...
    'elseif (vc >= vb) && (vb >= va)\n' ...
    '    sector = 4;\n' ...
    'elseif (vc >= va) && (va >= vb)\n' ...
    '    sector = 5;\n' ...
    'else\n' ...
    '    sector = 6;\n' ...
    'end\n' ...
    'duty_a = duty(1);\n' ...
    'duty_b = duty(2);\n' ...
    'duty_c = duty(3);\n' ...
    'switch sector\n' ...
    '    case {1, 6}\n' ...
    '        max_sampled = max(duty_b, duty_c);\n' ...
    '    case {2, 3}\n' ...
    '        max_sampled = max(duty_a, duty_c);\n' ...
    '    case {4, 5}\n' ...
    '        max_sampled = max(duty_a, duty_b);\n' ...
    '    otherwise\n' ...
    '        max_sampled = 0.0;\n' ...
    'end\n' ...
    'if max_sampled > duty_sample_limit\n' ...
    '    common_shift = -(max_sampled - duty_sample_limit);\n' ...
    '    min_duty = min([duty_a; duty_b; duty_c]);\n' ...
    '    if (min_duty + common_shift) < 0.0\n' ...
    '        common_shift = -min_duty;\n' ...
    '    end\n' ...
    '    duty_a = duty_a + common_shift;\n' ...
    '    duty_b = duty_b + common_shift;\n' ...
    '    duty_c = duty_c + common_shift;\n' ...
    'end\n' ...
    'duty = min(max([duty_a; duty_b; duty_c], 0.0), 1.0);\n' ...
    'da = duty(1);\n' ...
    'db = duty(2);\n' ...
    'dc = duty(3);\n' ...
    'end\n']);
end

function local_set_window_metrics_script(blockPath)
rt = sfroot;
chart = rt.find('-isa', 'Stateflow.EMChart', 'Path', blockPath);
chart.Script = sprintf([ ...
    'function [w_ideal, w_dead, w_valid] = fcn(da, db, dc, T_pwm, dead_time, settle_time)\n' ...
    '%% Low-side window metrics for center-aligned PWM\n' ...
    'd = [da; db; dc];\n' ...
    'w_ideal = zeros(3,1);\n' ...
    'w_dead = zeros(3,1);\n' ...
    'w_valid = zeros(3,1);\n' ...
    'for i = 1:3\n' ...
    '    w_ideal(i) = max((1.0 - d(i)) * T_pwm, 0.0);\n' ...
    '    w_dead(i) = max(w_ideal(i) - 2.0 * dead_time, 0.0);\n' ...
    '    w_valid(i) = max(w_dead(i) - settle_time, 0.0);\n' ...
    'end\n' ...
    'end\n']);
end

function local_set_deadtime_script(blockPath)
rt = sfroot;
chart = rt.find('-isa', 'Stateflow.EMChart', 'Path', blockPath);
chart.Script = sprintf([ ...
    'function [s_high, s_low] = fcn(pwm_cmd, dead_time, Ts_model)\n' ...
    '%% Complementary gate generation with dead time\n' ...
    'persistent active_state pending_state dead_count initialized\n' ...
    'if isempty(initialized)\n' ...
    '    active_state = double(pwm_cmd >= 0.5);\n' ...
    '    pending_state = active_state;\n' ...
    '    dead_count = 0;\n' ...
    '    initialized = true;\n' ...
    'end\n' ...
    'requested_state = double(pwm_cmd >= 0.5);\n' ...
    'samples_dt = max(1, ceil(dead_time / Ts_model));\n' ...
    'if dead_time <= 0.0\n' ...
    '    active_state = requested_state;\n' ...
    '    dead_count = 0;\n' ...
    'elseif dead_count > 0\n' ...
    '    pending_state = requested_state;\n' ...
    '    dead_count = dead_count - 1;\n' ...
    '    if dead_count == 0\n' ...
    '        active_state = pending_state;\n' ...
    '    else\n' ...
    '        active_state = -1;\n' ...
    '    end\n' ...
    'elseif requested_state ~= active_state\n' ...
    '    pending_state = requested_state;\n' ...
    '    active_state = -1;\n' ...
    '    dead_count = samples_dt;\n' ...
    'end\n' ...
    'if active_state == 1\n' ...
    '    s_high = true;\n' ...
    '    s_low = false;\n' ...
    'elseif active_state == 0\n' ...
    '    s_high = false;\n' ...
    '    s_low = true;\n' ...
    'else\n' ...
    '    s_high = false;\n' ...
    '    s_low = false;\n' ...
    'end\n' ...
    'end\n']);
end

function local_set_pwm_compare_script(blockPath)
rt = sfroot;
chart = rt.find('-isa', 'Stateflow.EMChart', 'Path', blockPath);
chart.Script = sprintf([ ...
    'function pwm_cmd = fcn(carrier, duty)\n' ...
    '%% Boundary-safe PWM compare for duty in [0, 1]\n' ...
    'if duty <= 0.0\n' ...
    '    pwm_cmd = false;\n' ...
    'elseif duty >= 1.0\n' ...
    '    pwm_cmd = true;\n' ...
    'else\n' ...
    '    pwm_cmd = carrier < duty;\n' ...
    'end\n' ...
    'end\n']);
end

function local_add_pwm_leg(mdl, phaseName, origin, dutySrc, carrierSrc, deadTimeSrc, tsSrc)

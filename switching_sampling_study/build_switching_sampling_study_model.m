add_block('simulink/User-Defined Functions/MATLAB Function', deadBlk, ...
    'Position', [origin(1)+80 origin(2)-5 origin(1)+205 origin(2)+55]);
local_set_deadtime_script(deadBlk);
add_line(mdl, ['Compare_' phaseName '/1'], ['DeadTime_' phaseName '/1']);
add_line(mdl, deadTimeSrc, ['DeadTime_' phaseName '/2']);
add_line(mdl, tsSrc, ['DeadTime_' phaseName '/3']);

add_block('simulink/Signal Routing/Goto', [mdl '/S_' phaseName '_high'], ...
    'Position', [origin(1)+240 origin(2) origin(1)+315 origin(2)+20], ...
    'GotoTag', ['S_' phaseName '_high']);
add_line(mdl, ['DeadTime_' phaseName '/1'], ['S_' phaseName '_high/1']);

add_block('simulink/Signal Routing/Goto', [mdl '/S_' phaseName '_low'], ...
    'Position', [origin(1)+240 origin(2)+35 origin(1)+315 origin(2)+55], ...
    'GotoTag', ['S_' phaseName '_low']);

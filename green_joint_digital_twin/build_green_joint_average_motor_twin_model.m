%% Build green-joint current-loop digital twin v1 with average motor model
%
% This model uses the repository's average-voltage motor-model pattern:
%
%   green_joint_current_loop_model Model Reference
%     -> DqToAbcDutyStep
%     -> Average-Value Inverter
%     -> Surface Mount PMSM
%     -> MotorClarkeParkStep feedback
%
% The controller core remains the green-joint PI-only MBD model reference.
% The inverter and PMSM are simulation plant blocks, not generated controller
% code.

clear;
clc;

script_dir = fileparts(mfilename('fullpath'));
repo_dir = fileparts(script_dir);

green_joint_mbd_dir = fullfile(repo_dir, 'green_joint_current_loop_mbd');
speed_mbd_dir = fullfile(repo_dir, 'motor_speed_pi_mbd');
speed_estimator_mbd_dir = fullfile(repo_dir, 'motor_speed_estimator_mbd');
motor_float_dir = fullfile(repo_dir, 'motor_float_open_loop_mbd');
clarke_park_dir = fullfile(repo_dir, 'motor_clarke_park_struct');

previous_dir = pwd;
cleanup_dir = onCleanup(@() cd(previous_dir));
cd(script_dir);
run(fullfile(script_dir, 'setup_green_joint_current_loop_twin.m'));
addpath(speed_mbd_dir);
addpath(speed_estimator_mbd_dir);
addpath(motor_float_dir);
addpath(clarke_park_dir);

model = 'green_joint_average_motor_twin_model';
model_file = fullfile(script_dir, [model '.slx']);

if should_open_existing_model_from_desktop(model_file)
    load_system(model_file);
    open_system(model);
    fprintf(['MATLAB Desktop detected. Opened existing average motor twin ' ...
        'without rebuilding:\n  %s\n'], model_file);
    return;
end

assert_safe_rebuild_environment(script_dir, model_file);
ensure_source_models(green_joint_mbd_dir, speed_mbd_dir, ...
    speed_estimator_mbd_dir, motor_float_dir, clarke_park_dir);
create_average_motor_twin_dictionary(script_dir);

source_green_joint_model = 'green_joint_current_loop_model';
source_speed_model = 'speed_pi_model';
source_speed_estimator_model = 'speed_estimator_pll_model';
source_float_model = 'motor_float_open_loop_model';
source_clarke_park_model = 'motor_clarke_park_model';

if bdIsLoaded(model)
    close_system(model, 0);
end

if exist(model_file, 'file')
    delete(model_file);
end

load_system(fullfile(green_joint_mbd_dir, [source_green_joint_model '.slx']));
load_system(fullfile(speed_mbd_dir, [source_speed_model '.slx']));
load_system(fullfile(speed_estimator_mbd_dir, ...
    [source_speed_estimator_model '.slx']));
load_system(fullfile(motor_float_dir, [source_float_model '.slx']));
load_system(fullfile(clarke_park_dir, [source_clarke_park_model '.slx']));
load_system('mcbplantlib');
load_system('mcblib');

cleanup_sources = onCleanup(@() close_source_models({ ...
    source_green_joint_model, source_speed_model, ...
    source_speed_estimator_model, source_float_model, ...
    source_clarke_park_model}));

new_system(model);
set_param(model, ...
    'DataDictionary', 'green_joint_average_motor_twin_interface.sldd', ...
    'StopTime', 'GJDT_StopTime', ...
    'SolverType', 'Fixed-step', ...
    'Solver', 'FixedStepDiscrete', ...
    'FixedStep', 'GJDT_TsPlant', ...
    'ParameterPrecisionLossMsg', 'none');

add_reference_sources(model);
add_referenced_controller_and_reuse_blocks(model, source_green_joint_model, ...
    source_speed_model, source_speed_estimator_model, source_float_model, ...
    source_clarke_park_model);
add_green_joint_output_adapter(model);
add_average_inverter_and_pmsm(model);
add_feedback_path(model);
add_logs(model);
connect_model(model);

save_system(model, model_file);

fprintf('Built green-joint average motor digital twin model:\n  %s\n', ...
    model_file);


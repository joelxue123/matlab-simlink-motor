function upsert_alias_type(section, type_name, base_type, header_file)
alias_type = Simulink.AliasType(base_type);
alias_type.DataScope = 'Exported';
alias_type.HeaderFile = header_file;

entry = find(section, 'Name', type_name);
if isempty(entry)
    addEntry(section, type_name, alias_type);
else
    setValue(entry(1), alias_type);
end
end

function upsert_bus_type(section, bus_name, header_file, element_specs)
bus_elements = repmat(Simulink.BusElement, numel(element_specs), 1);
for i = 1:numel(element_specs)
    bus_elements(i).Name = element_specs{i}{1};
    bus_elements(i).DataType = element_specs{i}{2};
    bus_elements(i).Dimensions = 1;
end

bus_type = Simulink.Bus;
bus_type.Elements = bus_elements;
bus_type.DataScope = 'Exported';
bus_type.HeaderFile = header_file;

entry = find(section, 'Name', bus_name);
if isempty(entry)
    addEntry(section, bus_name, bus_type);
else
    setValue(entry(1), bus_type);
end
end

function upsert_parameter(section, name, value, data_type, storage_class)
parameter = Simulink.Parameter(value);
parameter.DataType = data_type;
parameter.CoderInfo.StorageClass = storage_class;

entry = find(section, 'Name', name);
if isempty(entry)
    addEntry(section, name, parameter);
else
    setValue(entry(1), parameter);
end
end

function assert_safe_rebuild_environment(script_dir, model_file)
if strcmp(getenv('GJ_DT_ALLOW_UNSAFE_REBUILD'), '1')
    return;
end

if usejava('desktop')
    error(['This script rebuilds a generated .slx file. Run it from ' ...
        'matlab -batch, or set GJ_DT_ALLOW_UNSAFE_REBUILD=1 only after ' ...
        'closing the model in MATLAB Desktop.']);
end

if ~exist(model_file, 'file')
    return;
end

[status, output] = system(sprintf('lsof -F pc -- %s 2>/dev/null', ...
    shell_quote(model_file)));
if status ~= 0 || strlength(strtrim(string(output))) == 0
    return;
end

current_pid = feature('getpid');
lines = splitlines(strtrim(string(output)));
pid = NaN;
command_name = "";
holders = strings(0, 1);
for i = 1:numel(lines)
    line = lines(i);
    if startsWith(line, "p")
        pid = str2double(extractAfter(line, 1));
    elseif startsWith(line, "c")
        command_name = extractAfter(line, 1);
    end

    if ~isnan(pid) && command_name ~= ""
        if pid ~= current_pid && contains(command_name, "MATLAB")
            holders(end + 1, 1) = sprintf('%s held by PID %d (%s)', ...
                model_file, pid, command_name); %#ok<AGROW>
        end
        pid = NaN;
        command_name = "";
    end
end

if ~isempty(holders)
    error(['Another MATLAB process appears to hold the average motor twin. ' ...
        'Close it before rebuilding.\n%s'], strjoin(holders, newline));
end
end

function result = should_open_existing_model_from_desktop(model_file)
result = false;
if strcmp(getenv('GJ_DT_ALLOW_UNSAFE_REBUILD'), '1')
    return;
end

if usejava('desktop') && exist(model_file, 'file')
    result = true;
end
end


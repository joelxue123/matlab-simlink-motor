function set_matlab_function_code(block_path, code)
rt = sfroot;
chart = rt.find('-isa', 'Stateflow.EMChart', 'Path', block_path);
if isempty(chart)
    error('Could not find MATLAB Function chart: %s', block_path);
end
chart.Script = code;
end

function code = wrap_0_to_2pi_loop_code()
code = sprintf([ ...
    'function y = wrap_0_to_2pi_loop(u)\n' ...
    '%%#codegen\n' ...
    'two_pi = single(6.2831853071795864769);\n' ...
    'y = single(u);\n' ...
    'while y >= two_pi\n' ...
    '    y = y - two_pi;\n' ...
    'end\n' ...
    'while y < single(0.0)\n' ...
    '    y = y + two_pi;\n' ...
    'end\n' ...
    'end\n']);
end

function code = wrap_pi_loop_code()
code = sprintf([ ...
    'function y = wrap_pi_loop(u)\n' ...
    '%%#codegen\n' ...
    'pi_v = single(3.1415926535897932385);\n' ...
    'y = wrap_0_to_2pi_loop(single(u) + pi_v) - pi_v;\n' ...
    'end\n\n' ...
    'function y = wrap_0_to_2pi_loop(u)\n' ...
    '%%#codegen\n' ...
    'two_pi = single(6.2831853071795864769);\n' ...
    'y = single(u);\n' ...
    'while y >= two_pi\n' ...
    '    y = y - two_pi;\n' ...
    'end\n' ...
    'while y < single(0.0)\n' ...
    '    y = y + two_pi;\n' ...
    'end\n' ...
    'end\n']);
end

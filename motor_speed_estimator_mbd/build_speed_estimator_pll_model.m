%% Build green-joint speed-estimator PLL MBD module

clc;

cfg = customer_interface_config();
script_dir = fileparts(mfilename('fullpath'));
cfg.dictionaryFile = fullfile(script_dir, cfg.dictionaryName);
assignin('base', 'speed_estimator_pll_config', cfg);

previous_dir = pwd;
cleanup_dir = onCleanup(@() cd(previous_dir));
cd(script_dir);

defaults = default_speed_estimator_params();
assignin('base', 'speed_estimator_pll_defaults', defaults);
assignin('base', 'speed_estimator_pll_simcfg', defaults.simcfg);
assignin('base', 'speed_estimator_pll_test', defaults.test);

model = cfg.modelName;
model_file = fullfile(script_dir, [model '.slx']);

if bdIsLoaded(model)
    set_param(model, 'Dirty', 'off');
    close_system(model, 0);
end

close_open_data_dictionaries();
create_speed_estimator_data_dictionary(cfg, defaults);

if exist(model_file, 'file')
    delete(model_file);
end

new_system(model);
set_param(model, 'DataDictionary', cfg.dictionaryName);
set_param(model, ...
    'StopTime', '0.010', ...
    'SolverType', 'Fixed-step', ...
    'Solver', 'FixedStepDiscrete', ...
    'FixedStep', num2str(defaults.simcfg.sample_time_s, '%.12g'), ...
    'SystemTargetFile', 'ert.tlc', ...
    'ProdHWDeviceType', 'ARM Compatible->ARM Cortex', ...
    'TargetHWDeviceType', 'ARM Compatible->ARM Cortex', ...
    'GenerateReport', 'on', ...
    'RTWVerbose', 'on', ...
    'GenCodeOnly', 'on', ...
    'GenerateSampleERTMain', 'off', ...
    'SupportContinuousTime', 'off', ...
    'ParameterPrecisionLossMsg', 'none', ...
    'InlineParams', 'off', ...
    'CodeInterfacePackaging', 'Reusable function', ...
    'ModelReferenceNumInstancesAllowed', 'Multi');

add_speed_estimator_subsystem(model, cfg);
add_root_interface(model, cfg);

set_param(model, 'SimulationCommand', 'update');
save_system(model, model_file);

fprintf('Built speed-estimator PLL MBD model:\n  %s\n', model_file);

function cfg = customer_interface_config()
cfg.modelName = 'speed_estimator_pll_model';
cfg.sampleTime = '50e-6';
cfg.dictionaryName = 'speed_estimator_pll_interface.sldd';
cfg.typeHeaderFile = 'speed_estimator_pll_types.h';

cfg.floatTypeName = 'T_SpeedEstimatorFloat';
cfg.angleTypeName = 'T_SpeedEstimatorAngle';
cfg.speedTypeName = 'T_SpeedEstimatorSpeed';
cfg.gainTypeName = 'T_SpeedEstimatorGain';
cfg.resetTypeName = 'T_SpeedEstimatorReset';

cfg.inputBusName = 'speed_estimator_input_t';
cfg.outputBusName = 'speed_estimator_output_t';

cfg.floatBaseType = 'single';
cfg.resetBaseType = 'uint8';
end

function defaults = default_speed_estimator_params()
defaults.simcfg.sample_time_s = 50e-6;
defaults.pll_bandwidth_hz = 360.0;
defaults.pll_damping = 1.0;
omega_n = 2 * pi * defaults.pll_bandwidth_hz;
defaults.PllKp = single(2 * defaults.pll_damping * omega_n);
defaults.PllKi = single(omega_n * omega_n);
defaults.GearRatio = single(183.35);
defaults.InvGearRatio = single(1.0 / double(defaults.GearRatio));
defaults.PolePairs = single(2.0);
defaults.encoder_counts = single(65536.0);
defaults.encoder_offset_rad = single(0.0);
defaults.ZeroSpeedThresholdRadS = single( ...
    0.5 * defaults.simcfg.sample_time_s * double(defaults.PllKi) * ...
    (2 * pi / double(defaults.encoder_counts)));
defaults.test.speed_before_rad_s = single(0.0);
defaults.test.speed_after_rad_s = single(900.0);
defaults.test.stop_time = 0.030;
defaults.test.step_time = 0.005;
end

function close_open_data_dictionaries()
try
    Simulink.data.dictionary.closeAll('-discard');
catch err
    warning('Could not close open data dictionaries before rebuild: %s', ...
        err.message);
end
end

function create_speed_estimator_data_dictionary(cfg, defaults)
dd = open_or_create_data_dictionary(cfg);
design_data = getSection(dd, 'Design Data');

upsert_alias_type(design_data, cfg.floatTypeName, cfg.floatBaseType, cfg.typeHeaderFile);
upsert_alias_type(design_data, cfg.angleTypeName, cfg.floatBaseType, cfg.typeHeaderFile);
upsert_alias_type(design_data, cfg.speedTypeName, cfg.floatBaseType, cfg.typeHeaderFile);
upsert_alias_type(design_data, cfg.gainTypeName, cfg.floatBaseType, cfg.typeHeaderFile);
upsert_alias_type(design_data, cfg.resetTypeName, cfg.resetBaseType, cfg.typeHeaderFile);

upsert_bus_type(design_data, cfg.inputBusName, cfg.typeHeaderFile, { ...
    {'motor_angle_rad', cfg.angleTypeName}; ...
    {'reset', cfg.resetTypeName}});

upsert_bus_type(design_data, cfg.outputBusName, cfg.typeHeaderFile, { ...
    {'joint_speed_est_rad_s', cfg.speedTypeName}; ...
    {'motor_speed_mech_rad_s', cfg.speedTypeName}; ...
    {'motor_speed_elec_rad_s', cfg.speedTypeName}; ...
    {'theta_hat_rad', cfg.angleTypeName}});

upsert_parameter(design_data, 'PllKp', defaults.PllKp, cfg.gainTypeName);
upsert_parameter(design_data, 'PllKi', defaults.PllKi, cfg.gainTypeName);
upsert_parameter(design_data, 'SpeedEstimatorSampleTime', ...
    single(defaults.simcfg.sample_time_s), cfg.floatTypeName);
upsert_parameter(design_data, 'GearRatio', defaults.GearRatio, cfg.floatTypeName);
upsert_parameter(design_data, 'InvGearRatio', defaults.InvGearRatio, cfg.floatTypeName);
upsert_parameter(design_data, 'PolePairs', defaults.PolePairs, cfg.floatTypeName);
upsert_parameter(design_data, 'EncoderOffsetRad', ...
    defaults.encoder_offset_rad, cfg.angleTypeName);
upsert_parameter(design_data, 'ZeroSpeedThresholdRadS', ...
    defaults.ZeroSpeedThresholdRadS, cfg.speedTypeName);

saveChanges(dd);
close(dd);
end

function dd = open_or_create_data_dictionary(cfg)
if exist(cfg.dictionaryFile, 'file')
    dd = Simulink.data.dictionary.open(cfg.dictionaryFile);
else
    dd = Simulink.data.dictionary.create(cfg.dictionaryFile);
end
end

function upsert_alias_type(section, type_name, base_type, header_file)
alias_type = Simulink.AliasType(base_type);
alias_type.DataScope = 'Exported';
alias_type.HeaderFile = header_file;
entry = find(section, 'Name', type_name);
if isempty(entry)
    addEntry(section, type_name, alias_type);
else
    setValue(entry(1), alias_type);
end
end

function upsert_bus_type(section, bus_name, header_file, element_specs)
bus_elements = repmat(Simulink.BusElement, numel(element_specs), 1);
for i = 1:numel(element_specs)
    bus_elements(i).Name = element_specs{i}{1};
    bus_elements(i).DataType = element_specs{i}{2};
    bus_elements(i).Dimensions = 1;
end
bus_type = Simulink.Bus;
bus_type.Elements = bus_elements;
bus_type.DataScope = 'Exported';
bus_type.HeaderFile = header_file;
entry = find(section, 'Name', bus_name);
if isempty(entry)
    addEntry(section, bus_name, bus_type);
else
    setValue(entry(1), bus_type);
end
end

function upsert_parameter(section, name, value, data_type)
parameter = Simulink.Parameter(double(value));
parameter.DataType = data_type;
parameter.CoderInfo.StorageClass = 'Auto';
entry = find(section, 'Name', name);
if isempty(entry)
    addEntry(section, name, parameter);
else
    setValue(entry(1), parameter);
end
end

